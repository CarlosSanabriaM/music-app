        System.out.print("A単adimos la cancion: "+cancionSeleccionada[0]);
        
        String s = listaAAPL.getSelectedValue();
        if(s=="Coche"){
            //A単adimos a la tabla Coche
            DefaultTableModel DTM = (DefaultTableModel) VP.tablaPLCoche.getModel();
            DTM.addRow(cancionSeleccionada);
        }
        else if(s=="Favoritos"){
            //A単adimos a la tabla Coche
            DefaultTableModel DTM = (DefaultTableModel) VP.tablaPLFavoritos.getModel();
            DTM.addRow(cancionSeleccionada);
        }
        
